			<h2>Couture &amp; Wedding</h2>
			<div class="txt col1">
				<p>No other designer in the history of British Couture captivated London society the way Norman Hartnell did. Hartnell had a remarkable ability to create expressive designs, which both enhanced the individuality of the wearer, and sparked the imagination of the viewer.</p>
				<p>Contemporaries such as Chanel and Christian Dior regarded him as a design inspiration. The British couturier was also the favourite designer of stage and screen stars Vivien Leigh and Marlene Dietrich, who were drawn to his Bruton Street salon.</p>
				<p>Hartnell’s love for spectacle also found expression in his opulent wedding dresses. He was praised for his ability to create ethereal and romantic gowns which elevated character and body type. Brides from both sides of the Atlantic clamoured to have their gowns created by the designer.</p>
