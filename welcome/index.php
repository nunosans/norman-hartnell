			<div class="txt col3">
				<p>It was not uncommon for the House of Hartnell to design for the entire bridal retinue. This dazzling panoply included dresses for the bridesmaids, the mother of the bride, the honeymoon wardrobe and trousseau. On occasion Hartnell would also dress the Groom’s family.</p>
				<p>When the Duke and Duchess of Gloucester married in 1935, Hartnell designed both the Duchesses’ pearl pink satin wedding dress, and the dresses for her bridesmaids, Princess Elizabeth and Princess Margaret. This marked the beginning of his long and illustrious relationship with the Royal Family.</p>
				<p>Hartnell was instrumental in helping the Queen Mother craft a sense of romanticism in her style. She became such an avid fan of Hartnell that she insisted he design bridal gowns for both her daughters, Princess Elizabeth in 1947, and Princess Margaret in 1960. Hartnell claimed that his ivory silk, crystal and seed pearl embroidered gown for Princess Elizabeth was “the most beautiful dress” he had made to date. The 13- foot-long star-patterned train was inspired by Botticelli’s Primavera, and captured the imagination of a stricken post-war Britain in search of escapism.</p>
