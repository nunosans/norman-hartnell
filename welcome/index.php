			<h2>History</h2>
			<div class="txt col1 left">
				<p>Sir Norman Hartnell combined flamboyant flair with the dignity and assurance of traditional British style. The designer who famously quipped, “I despise simplicity. It is the negation of all that is beautiful” was known for his opulent yet elegant designs, lavishly adorned embroidery, and use of intricate details. His repertoire of clients included Hollywood starlets, socialites, princesses and queens, all of whom were drawn to the glamour and palatial pomp exuded by his designs.</p>
				<p>Hartnell began his career in fashion by designing costumes for the legendary Cambridge University Footlights Dramatic Club. After leaving Cambridge without a degree, he set up his eponymous label in 1923 on Bruton Street in Mayfair. Although best known as a couturier and official dressmaker to the Queen, Hartnell produced a range of collections over the course of his lifetime, including bridal wear, perfume, shoes, furs, menswear, jewellery and ready-to-wear.</p>
			</div>
			<img class="col2 auto-height before index" src="../images/history-painting-1.jpg" alt="Norman Hartnell’s final sketch for Queen Elizabeth II’s coronation gown" title="Norman Hartnell’s final sketch for Queen Elizabeth II’s coronation gown" width="745" height="1006">
			<img class="col2 auto-height after clear index" src="../images/history-hartnell.jpg" alt="The House of Hartnell, 26 Bruton St. Mayfair." title="The House of Hartnell, 26 Bruton St. Mayfair." width="745" height="1023">
			<div class="txt col1 right">
				<p>His most famous commissions included his designs for Queen Elizabeth’s wedding dress in 1947, and his highly celebrated Coronation gown 6 years later. The Coronation gown, which was hand embroidered with 10,000 seed pearls and thousands of white crystal beads, all meticulously arranged to render emblems of the Commonwealth, is widely regarded today as a centerpiece in the history of ceremonial dress. The resplendent gown will be on display at Buckingham Palace this June as part of the Queen’s Diamond Jubilee celebrations.</p>
				<p>The Queen Mother knighted Hartnell in 1977 for his services to the Royal Household. He became known as ‘The First Fashion Knight’, and was one of only four British designers to ever have been knighted; Norman Hartnell, Hardy Amies, Paul Smith and Vivienne Westwood.</p>
